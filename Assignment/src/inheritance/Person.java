package inheritance;

  // Single level inheritance -> by using setter getter

public class Person {
	
	int id, age;
	String name;
	
	public void setId(int id)
	{
		this.id = id;
	}

	public void setAge(int age)
	{
		this.age = age;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
		
}

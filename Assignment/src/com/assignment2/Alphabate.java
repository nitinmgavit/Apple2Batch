package com.assignment2;

public class Alphabate {

	public static void main(String[] args) {
		// 5) Write a program to print all alphabets from a to z  using  for loop
		
		char ch;
		for (ch='a'; ch<='z'; ch++)
			System.out.print(ch+ " ");		
	}

}






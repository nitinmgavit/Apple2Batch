package demo;

// for loop

public class Loops_1 {
	 
	public static void main(String args[]) {
		
		for (int counter=0;     counter<=3;     counter=counter +1) {
			  //initialization;  condition;      updation/increment/decrement
			
			System.out.println("Hello World");
			
			/* for (int counter=0; ; counter=counter+1)
			 in this case if we do not define "terminating condition" our loop will be continuously 
			 print "Hello World" until the computer memory is full.
			  
			 */
			
		}
		
	}

}

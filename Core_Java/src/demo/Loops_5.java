package demo;

// do while 

public class Loops_5 {
	
	public static void main(String args []) {
		
		int i=1;
		
		do {
			System.out.print(i+ ", ");			
			i++;
			
		} while (i<=10);
		
	}

}  /* in for loop and while loop you have to check the condition at first 
      but in do while loop you have to work first and then check the condition 
      
      it means in for loop and while if condition is false at first nothing will print
      but in do while loop either condition is true or false output will be print at least once.
 */


package InheritanceSingle;

public class Animal { // Parent class
	
	void eat()
	{
		System.out.println("Eating..");
	}

}


/* 

When a class inherits another class, it is known as a single inheritance. 
In the example given below, Dog class inherits the Animal class, so there is the single inheritance.

When a child class inherits the properties of parent class that is inheritance.
In the given example Dog class inherits the Properties of Animal class, so there is a single inheritance.

*/